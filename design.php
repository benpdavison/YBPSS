	<h2 class="P10">
		<a id="a__Content"><span /></a>Content
	</h2>
	<p class="P5">All of the pages that where described in the
		specification of content have been implemented on the site, as well as
		a full copy of the YBPSS website. The reason for using all the pages
		from the YBPSS site rather than the basic implementation was so that,
		whilst building, I could see how the changes that I applied affected
		the resulting pages. Thus further informing the design choices made.</p>
	<h2 class="P10">
		<a id="a__Accessibility"><span /></a>Accessibility
	</h2>
	<p class="P6">The YBPSS website created validates with no errors
		against the HTML5 standard. There are multiple errors however when
		validating the CSS. This is only due to the use of external CSS such
		as bootstrap. No errors were found in the CSS written specifically for
		YBPSS.</p>
	<p class="P6">
		<br />With regard to the semantic mark-up of the website, at no point
		were tables, or any other element design to have specific textual
		semantics, used for the layout of the site. If content required the
		use of a list, then a list was used. This can be seen in the layout of
		the document. It may have been easier to use a table to give the
		tabular style that the website uses, however, rather than use
		incorrect mark-up for the layout of the website, <span class="T9">&lt;div&gt;</span>s
		were used instead. These utilised bootstraps column features which
		enable the programmer to choose from a variety of width profiles for
		the columns, as well as specify the behaviour of the columns when the
		width of the page is reduced. <br />Alternatives were initially a
		tricky problem. As the content would be uploaded primarily by the
		user, then there would be no way to enforce the use of <span
			class="T9">&lt;alt&gt;</span> tags within the image uploads. However,
		by using a form and requiring the user to upload a description of the
		image, all images displayed on the website can have an <span
			class="T9">&lt;alt&gt;</span> tag. There is no guarantee of the
		quality of the <span class="T9">&lt;alt&gt;</span> tags, but as the
		web admin is able to review the upload of the user, before they go
		live on the website, that person would feasibly, be able to alter the
		description of the image the user uploaded. Video was managed directly
		by the web admin, therefore they can manually enter <span class="T9">&lt;alt&gt;</span>
		tag information, the example video does have one.
	</p>
	<p class="P6">
		<br />All form elements of the website have labels which distinguish
		the functionality of each form input. Buttons have a clear name which
		is contained within the button itself, so as to distinguish its
		functionality. Any other interactive components that are present on
		the site, such as the download sections of the newsletter page, have
		clearly defined title attributes which show when the user hovers over
		the element.
	</p>
	<p class="P6">
		<br />Lastly, all of the elements are laid out in a logical and linear
		order, CSS is used to manipulate the display if the elements, but if
		this was to be turned off, then the content would display in a linear
		and logical way. This enables the user to tab through the page and get
		the expected result. In addition to this, there is drop-down sub-menus
		on the menu bar. These are controlled with JavaScript, via Adobes
		Accessible Mega Menu. This plug-in allows the menu to be fully
		keyboard controllable, utilising the up, down, left, right buttons, as
		well as the tab buttons.
	</p>
	<h2 class="P10">
		<a id="a__Colour_Scheme_Selection"><span /></a>Colour Scheme Selection
	</h2>
	<p class="P5"> </p>
	<p class="P6">To eliminate the catch 22 problem of the user not
		being able to find the change colour scheme option if they were
		struggling to use the site. The link to the page was placed at the top
		of the page, to make it clear and provide ease of access.</p>
	<p class="P8">
		<br />The implementation of the colour scheme selection uses 3 main
		features, both are equally reliant on JavaScript. This is the primary
		downfall to this part of the website. If JavaScript was to be disabled
		the colour scheme selection would not work at all. <br />The first
		feature of the page is the ability for the user to provide custom
		colours for the website. This script is taken from the spectrum.js*
		plug-in. The plug-in gives the ability to fully customise the way in
		which the user selects the colour, however, the default display was
		sufficient for this application. When the user finishes to select the
		colours that they are applying to the site, they can press the "Apply
		Colour" button. This then calls the colorChange.js file. The only part
		of this file that is not written by myself is the "compareColours"
		function, which works by applying the colour to two &lt;div&gt;
		elements to normalize the format of the colour, this can then be used
		to compare whether they are the same. This function is only used to
		ensure the user does not select the same colour for both the text and
		background colour, as this may render the website unusable until the
		user cleared their cache. The functionality of this check is limited
		however, as the colour could be one shade lighter and the check would
		return false, allowing the application of the colours, even though the
		screen would be unreadable. <br />The section feature in on this page
		is the application of colours. This works by using jQuery to select
		all of the elements that need to be changed, this is done by manually
		entering all of the required elements. When the apply colour button is
		pressed, the "changeToCustomColor" function is called, which gets the
		colours within the colour selection boxes, using the in-built function
		.spectrum(get) which was provided with the spectrum source code. After
		the colours are set to variables, the variables are then passed to the
		"changeColor" function (if the user presses a default colour option,
		“changeColor” is called directly) which uses the variables to apply
		the colours to the jQuery objects mentioned earlier via the jQuery
		.css() function. After the colours are set to variables, the variables
		are then passed to the "changeColor" function (if the user presses a
		default colour option, "changeColor" is called directly) which uses
		the variables to apply the colours to the jQuery objects mentioned
		earlier via the jQuery .css() function.
	</p>
	<p class="P8">
		 <br />Finally, after all the colours have been applied, the result is
		displayed on the page for the user to see. However, the Colour scheme
		will not be saved until the user presses the save colour scheme
		button. This then passes the current colours to the local storage of
		the browser. This is covered in the cookies section of this document.
	</p>
	<h2 class="P10">
		<a id="a__Responsiveness"><span /></a>Responsiveness
	</h2>
	<p class="P6">
		Responsiveness was primarily implemented sing Bootstrap. Utilising the
		column options that is provides. These allow the designer to assign a
		portion of the parent &lt;div&gt; to the content of the child <span
			class="T9">&lt;div&gt;</span>. Additionally, upon different media
		queries the layout of the columns will change. Columns were user for
		the content and sidebar elements, the responsive nature of them can be
		seen through how their layout changes with the changing sizes of the
		screen. Although this could have probably been implemented manually,
		Bootstrap offered a clean and easy way to implement this.
	</p>
	<p class="P6">
		<br />Manual entry of media queries was another way responsiveness was
		implemented. Using the de facto sizes to control the appearance of the
		menu bar at different widths. <br />One problem that was encountered
		was with the resizing of the video, on the video page. Although with
		JavaScript disabled the video was responsive, as it should be with
		width set to 100%, when JavaScript was re-enabled the video no longer
		scaled correctly. The cause of this is uncertain, but to control this,
		a small script was written to set the size of the video on load and
		change the size on the event that the screen size was changed.
	</p>
	<h2 class="P10">
		<a id="a__Video"><span /></a>Video
	</h2>
	<p class="P6">
		The video player that was implemented meets all the requirements
		outlined in the specification. It provides subtitles or closed
		captions, as long as the user uploads a <span class="T9">.vtt</span>
		file and also provides audio description. The audio description relies
		on the use of an alternate video file that already has the audio
		description applied.
	</p>
	<p class="P6">
		<br />The video player is provided by <span class="T9">video.js</span>.
		It natively supports the use of <span class="T9">.vtt</span> files for
		closed caption or subtitles, although the closed captions that are
		currently enabled on the example video are actually subtitles.
	</p>
	<p class="P6">
		<br />The audio description function is implemented by manipulating
		another plug-in that was available for <span class="T9">video.js</span>.
		The original plug-in was used to switch the video content between
		standard definition and high definition. However, manipulating the
		code to <span class="T9">AD</span> rather than <span class="T9">HD</span>,
		so as to make the code clearer, and then rather than use varying
		quality footage, replaced the <span class="T9">src</span> attributes
		to include the audio described and non-audio described version of the
		video. The <span class="T9">src</span> links to each version of the
		video, instead of being placed inside the content tags of the HTML5
		video as standard coding convention would advise, the two differing
		sources for the audio described and non-audio described version are
		instead placed as attributes in the <span class="T10">&lt;</span><span
			class="T9">video</span><span class="T10">&gt;</span> tag. This way
		the JavaScript can get the content of the attribute when the change
		function is called. The only issue with the implementation of this
		plug-in, is that on the pressing of the button, the video will restart
		from the beginning.
	</p>
	<h2 class="P10">
		<a id="a__Cookies"><span /></a>Cookies
	</h2>
	<p class="P6">To comply with European law there is a banner
		displayed on the main page upon first visit. This banner informs the
		user that the website does use cookies, and that by proceeding they
		are agreeing to the use of cookies by this website. This follows the
		Information Commissioners guidance which stated that “although an
		explicit opt-in mechanism might provide regulatory certainty it was
		not the only means of gaining consent. In some circumstances those
		seeking consent might consider implied consent as an option that was
		perhaps more practical than the explicit opt-in model.”* Additionally
		due to the fact that the cookies contain no personal information, the
		argument for the use of implied consent is stronger.</p>
	<p class="P6">
		<br />The cookie is implemented, primarily as a mechanism for the user
		to save the colour scheme that they have selected for the website, the
		only case where this is not true is that the website uses a cookie to
		store whether or not the user has been displayed the cookie warning
		message.
	</p>
	<p class="P6">
		<br />To store the information about the colour scheme that the user
		has selected the HTML5 local storage functionality was used. This
		functionality of storing name value pairs in the browser offers
		benefits such as better security and the ability to hold more data*.
		Once the user presses the save colour scheme button. The name value
		pairs for each of each of the variables passed are sent to the browser
		storage. This means that the JavaScript function that runs on the load
		of each page is able to see those variables and load the correct
		colours for the user to view the website.
	</p>
	<h2 class="P10">
		<a id="a__Contributions"><span /></a>Contributions
	</h2>
	<p class="P6">
		Contributions to the website were especially important as they were
		mentioned multiple times in the specification and ion the personas. To
		aid in this a simple contact us form was provided. The form upon
		completion and validation emails the web admin with the contact query.
		Additionally a <span class="T9">mailto</span> link was provided to the
		user in case they preferred to use their own email client to contact
		the society.
	</p>
	<p class="P6">
		<br />Finally, a content upload system was implemented to allow the
		upload of images to the website. This works by using a form input,
		from which the user is able to browse for the file they wish to upload
		and enter the information about that image. This information is then
		validated and made safe by inserting slashes etc. The data is then
		stored on the server. This allows the web admin to control whether or
		not the data is displayed to the user. This is done using an integer
		variable as a Boolean. The database table has an “<span class="T9">imgOK</span>”
		column which controls the displaying of images. When the photo is
		first uploaded the photo will be set with an integer of 1, then when
		the photo has been reviewed and accepted this value will then be
		updated to 0. The photo gallery page will only show images that have a
		value of 0 in their “<span class="T9">imgOK</span>” column. The images
		are generated by taking the image data stored in the database, and
		then writing the content to an HTML page. This page serves as the
		location of the image, should any person need to provide a hyperlink
		to the image (this is implemented in the <span class="T9">img.php</span>
		file).
	</p>
